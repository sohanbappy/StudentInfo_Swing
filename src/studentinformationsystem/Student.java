    //Take screenshot
    public static BufferedImage getScreenShot(Component component){
        BufferedImage image = new BufferedImage(component.getWidth(), component.getHeight(), BufferedImage.TYPE_INT_RGB);
        component.paint(image.getGraphics());
        return image;
    }
    
    //Save screenshot in png format
    public static void saveScreenShot(Component component, String filename) throws Exception{
        BufferedImage img = getScreenShot(component);
        ImageIO.write(img, "png", new File(filename));
    }
    
    //Get ID from login
    public Student(int ID) {
        initComponents();
        
        //Create database connection
        dbMysqlConnection ob = new dbMysqlConnection();
        Connection conn = null; 
	conn = ob.getConnection();
        
        Statement statement1 = null;
        Statement statement2 = null;
        ResultSet resultSet1 = null;
        ResultSet resultSet2 = null;
       
        //SQL query 
        String query1 = "select id, name, fa_name, mo_name, mobile, mail, bl_gr, address, dof from st_general where id = '"+ID+"'";
        String query2 = "select id, prog, sem, cgpa, res_st from st_academic where id = '"+ID+"'";
        
        try{
            statement1 = conn.createStatement();
            statement2 = conn.createStatement();
            resultSet1 = statement1.executeQuery(query1);
            resultSet2 = statement2.executeQuery(query2);
            
            //Show general information
            if(resultSet1.next()) {
                String Id = resultSet1.getString("id");
                jLabel_ShowId.setText(Id);
                String name = resultSet1.getString("name");
                jLabel_ShowName.setText(name);
                String father_name = resultSet1.getString("fa_name");
                jLabel_ShowFatherName.setText(father_name);
                String mother_name = resultSet1.getString("mo_name");
                jLabel_ShowMotherName.setText(mother_name);
                String mobile = resultSet1.getString("mobile");
                jLabel_ShowMobile.setText(mobile);
                String email = resultSet1.getString("mail");
                jLabel_ShowEmail.setText(email);
                String blood_group = resultSet1.getString("bl_gr");
                jLabel_ShowBloodGroup.setText(blood_group);
                String birth_date = resultSet1.getString("dof");
                jLabel_ShowDateOfBirth.setText(birth_date);
                String address = resultSet1.getString("address");
                jTextArea_Address.setText(address);
            }
            
            //Show academic information
            if(resultSet2.next()) {
                String program = resultSet2.getString("prog");
                jLabel_ShowProgram.setText(program);
                String semester = resultSet2.getString("sem");
                jLabel_ShowSemester.setText(semester);
                String cgpa = resultSet2.getString("cgpa");
                jLabel_ShowCGPA.setText(cgpa);
                String resultstatus = resultSet2.getString("res_st");
                jLabel_ShowResultStatus.setText(resultstatus);
            }
            
            statement1.close();
            statement2.close();
            conn.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

