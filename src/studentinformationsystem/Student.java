    //Make PDF report
    private void jButton_PdfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_PdfActionPerformed
        try{
            //Variable declaration
            String id = jLabel_ShowId.getText();
            String name = jLabel_ShowName.getText();
            String father_name = jLabel_ShowFatherName.getText();
            String mother_name = jLabel_ShowMotherName.getText();
            String mobile= jLabel_ShowMobile.getText();
            String email = jLabel_ShowEmail.getText();
            String blood_group = jLabel_ShowBloodGroup.getText();
            String birth_date = jLabel_ShowDateOfBirth.getText();
            String address = jTextArea_Address.getText();
            String program = jLabel_ShowProgram.getText();
            String semester = jLabel_ShowSemester.getText();
            String cgpa = jLabel_ShowCGPA.getText();
            String result_status = jLabel_ShowResultStatus.getText();
           
            Document document = new Document();
            PdfWriter.getInstance(document, new FileOutputStream("Report.pdf"));
            document.open();
            //document.add(new Paragraph("STUDENT INFORMATION SYSTEM"));
            
            PdfPTable table = new PdfPTable(2);
            PdfPCell cell = new PdfPCell(new Paragraph("Report"));
            cell.setColspan(8);
            cell.setHorizontalAlignment(Element.ALIGN_CENTER);
            cell.setPadding(10.0f);
            table.addCell(cell);
        
            table.addCell("ID");
            table.addCell(id);
            table.addCell("Name");
            table.addCell(name);
            table.addCell("Father's Name");
            table.addCell(father_name);
            table.addCell("Mother's Name");
            table.addCell(mother_name);
            table.addCell("Mobile");
            table.addCell(mobile);
            table.addCell("Email");
            table.addCell(email);
            table.addCell("Blood Group");
            table.addCell(blood_group);
            table.addCell("Date of Birth");
            table.addCell(birth_date);
            table.addCell("Address");
            table.addCell(address);
            table.addCell("Program");
            table.addCell(program);
            table.addCell("Semester");
            table.addCell(semester);
            table.addCell("CGPA");
            table.addCell(cgpa);
            table.addCell("Result Status");
            table.addCell(result_status);
           
            document.add(table);
            document.add(new Paragraph(new Date().toString()));
            document.close();
            
            JOptionPane.showMessageDialog(null, "Report Generated");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton_PdfActionPerformed

